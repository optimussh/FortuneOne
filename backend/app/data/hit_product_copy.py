def apply_hit_copy(product: dict) -> dict:
    hid = product.get("id")
    if hid not in HIT_COPY:
        return product
    extra = HIT_COPY[hid]
    out = {**product, **extra, "hit": True, "copy_version": max(3, int(product.get("copy_version") or 0))}
    return out
